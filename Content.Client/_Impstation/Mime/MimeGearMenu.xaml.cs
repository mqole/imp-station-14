using Content.Client.UserInterface.Controls;
using Content.Shared._Impstation.Mime;
using Robust.Client.AutoGenerated;
using Robust.Client.GameObjects;
using Robust.Client.UserInterface.XAML;

namespace Content.Client._Impstation.Mime;

/// <summary>
/// The UI for the mime performance ui.
/// </summary>
[GenerateTypedNameReferences]
public sealed partial class MimeGearMenu : FancyWindow
{
    [Dependency] private readonly IEntitySystemManager _sysMan = default!;
    private readonly SpriteSystem _spriteSystem;

    public event Action? OnApprove;
    public event Action<int>? OnSetChange;

    /// <summary>
    /// Set up the required systems and the approve button.
    /// </summary>
    public MimeGearMenu()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
        _spriteSystem = _sysMan.GetEntitySystem<SpriteSystem>();

        ApproveButton.OnPressed += args =>
        {
            OnApprove?.Invoke();
        };
    }

    /// <summary>
    /// Get every possible gear and make a button for each, then set text to the supplied values.
    /// </summary>
    public void UpdateState(MimeGearMenuBoundUserInterfaceState state)
    {
        SetsGrid.DisposeAllChildren();
        var selectedNumber = 0;
        foreach (var (set, info) in state.Sets)
        {
            var child = new MimeGearMenuSet(info, _spriteSystem);

            child.SetButton.OnButtonDown += (args) =>
            {
                OnSetChange?.Invoke(set);
            };

            SetsGrid.AddChild(child);

            if (info.Selected)
                selectedNumber++;
        }

        Description.Text = Loc.GetString("mime-gearmenu-window-description");
        SelectedSets.Text = Loc.GetString("mime-gearmenu-window-selected", ("selectedCount", selectedNumber), ("maxCount", state.MaxSelectedSets));
        ApproveButton.Disabled = selectedNumber != state.MaxSelectedSets;
    }
}
